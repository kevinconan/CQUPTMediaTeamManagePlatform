public class MeetingBean {
	private String MEETING_TIME;
	private int MEETING_ID;
	private String MEETING_NAME;
	private String MEETING_CONTENT;
	private String MEETING_RECPEO;
	private String MEETING_PEONUM;
	public String getMEETING_TIME() {
		return MEETING_TIME;
	}
	public void setMEETING_TIME(String mEETING_TIME) {
		MEETING_TIME = mEETING_TIME;
	}
	public int getMEETING_ID() {
		return MEETING_ID;
	}
	public void setMEETING_ID(int mEETING_ID) {
		MEETING_ID = mEETING_ID;
	}
	public String getMEETING_NAME() {
		return MEETING_NAME;
	}
	public void setMEETING_NAME(String mEETING_NAME) {
		MEETING_NAME = mEETING_NAME;
	}
	public String getMEETING_CONTENT() {
		return MEETING_CONTENT;
	}
	public void setMEETING_CONTENT(String mEETING_CONTENT) {
		MEETING_CONTENT = mEETING_CONTENT;
	}
	public String getMEETING_RECPEO() {
		return MEETING_RECPEO;
	}
	public void setMEETING_RECPEO(String mEETING_RECPEO) {
		MEETING_RECPEO = mEETING_RECPEO;
	}
	public String getMEETING_PEONUM() {
		return MEETING_PEONUM;
	}
	public void setMEETING_PEONUM(String mEETING_PEONUM) {
		MEETING_PEONUM = mEETING_PEONUM;
	}

}
